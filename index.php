        <h3>Очки Виртуальной реальности</h3>
        <p>Шлем виртуальной реальности (англ. Head-mounted display) — устройство, позволяющее частично погрузиться в мир виртуальной реальности, создающее зрительный и акустический эффект присутствия в заданном управляющим устройством (компьютером) пространстве. Представляет собой конструкцию, надеваемую на голову, снабженное видеоэкраном и акустической системой. Название «шлем» достаточно условное: современные модели гораздо больше похожи на очки, чем на шлем.

Шлем создаёт объёмное изображение, демонстрируя два изображения — по одному для каждого глаза. Кроме того, шлем может содержать гироскопический или инфракрасный датчик положения головы.

В 2000-х и 2010-х годах шлемы виртуальной реальности значительно подешевели и ожидается, что в скором времени превратятся в такой же необходимый атрибут компьютерных игр, как джойстик или мышь. На рынке уже представлено несколько доступных моделей видеоочков по цене, не превышающей стоимость хорошего руля для гоночных компьютерных игр. В основных недостатках бюджетных моделей — отсутствие встроенного трекера для отслеживания позиции головы, низкое разрешение видеоэкранов. Некоторые дешевые видеоочки 2000-х годов всегда подают одинаковые изображения в оба глаза и не способны создавать трёхмерное изображение.</p>
